public class Main {
    public static void main(String[] args) {

        Integer five = 5;
        Integer[] others = {0, 5, 10, -50, 50};

        for (Integer i : others) {
            int val = five.compareTo(i);
            System.out.println("%d %s %d: compareTo=%d%n" +
                    (val == 0 ? "==" : (val < 0) ? "<" : ">"));
        }
    }
}